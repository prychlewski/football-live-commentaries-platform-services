<?php

namespace App\Command\Team;

use App\Command\DeleteCommandInterface;

class DeleteTeamCommand implements DeleteCommandInterface
{
    /**
     * @var int
     */
    private $id;

    public function __construct(int $id)
    {
        $this->id = $id;
    }

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }
}
